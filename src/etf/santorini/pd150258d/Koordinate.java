package etf.santorini.pd150258d;

public class Koordinate {

	private int red, kolona;

	public Koordinate(int r, int k) {
		red = r;
		kolona = k;
	}

	public int getRed() {
		return red;
	}

	public void setRed(int red) {
		this.red = red;
	}

	public int getKolona() {
		return kolona;
	}

	public void setKolona(int kolona) {
		this.kolona = kolona;
	}

}
